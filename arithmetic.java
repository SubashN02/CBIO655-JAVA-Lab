
public class arithmetic {

	public static void main(String[] args) {
		int a,b,add,sub,mult;
		float div;
		a=10;
		b=5;
		add=a+b;
		sub=a-b;
		mult=a*b;
		div=a/b;
		System.out.println("\t"+"Mathematical operations");
		System.out.println("\t"+"-----------------------");
		System.out.println("The two numbers taken for operations are "+a+" and "+b+".");
		System.out.println("Addition of "+a+" and "+b+" is : "+add);
		System.out.println("Subtraction of "+a+" and "+b+" is : "+sub);
		System.out.println("Multiplication of "+a+" and "+b+" is : "+mult);
		System.out.println("Division of "+a+" and "+b+" is : "+div);
	}
}
